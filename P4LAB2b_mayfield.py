
import turtle
# Initial for MM
wn = turtle.Screen()
wn.bgcolor("blue")
wn.title("Initals")
michael = turtle.Turtle()
michael.pensize(15)
michael.pencolor("white")
michael.shape("turtle")

#initial M
michael.penup()
michael.goto(-100,100)
michael.pendown()
michael.right(90)
michael.forward(150)
michael.backward(150)

michael.left(45)
michael.forward(90)

michael.left(95)
michael.forward(90)

michael.right(140)
michael.forward(150)



